// var, let e const
// var -> ES5 - usada em todos os browser
// let e const -> ES2015 - pode ser usadas para também escrever variáveis e praticamente todos os browser já estão interpretando-os.

let teste = 'minha string'
console.log(teste)

teste1 = 10
console.log(teste1)

const teste2 = 20
console.log(teste2)

// O let e o const não permite utilizar o mesmo nome de uma variável existente ao contrario do var
